<?php namespace Dick\MenuManager;

use Illuminate\Support\ServiceProvider;
use Illuminate\Routing\Router;

class MenuManagerServiceProvider extends ServiceProvider
{
    /**
     * Indicates if loading of the provider is deferred.
     *
     * @var bool
     */
    protected $defer = false;

    /**
     * Perform post-registration booting of services.
     *
     * @return void
     */
    public function boot()
    {
        // use this if your package has views
        $this->loadViewsFrom(realpath(__DIR__.'/resources/views'), 'menumanager');
    }

    /**
     * Define the routes for the application.
     *
     * @param  \Illuminate\Routing\Router  $router
     * @return void
     */
    public function setupRoutes(Router $router)
    {
        $router->group(['namespace' => 'Dick\MenuManager\Http\Controllers'], function($router)
        {
            require __DIR__.'/Http/routes.php';
        });
    }

    /**
     * Register any package services.
     *
     * @return void
     */
    public function register()
    {
        $this->registerMenuManager();
        $this->setupRoutes($this->app->router);
    }

    private function registerMenuManager()
    {
        $this->app->bind('menumanager',function($app){
            return new MenuManager($app);
        });
    }
}